                <p>Din preţioasa Bibliotecă Miclăuşeană s-au păstrat doar câteva mii de cărţi, multe deteriorate (dintre care cca. 4000 de volume se găsesc la Episcopia Romanului şi alte câteva sute la Biblioteca Universitară din Iaşi), mii de scrisori şi documente (actualmente în posesia Arhivelor Statului). Celelalte volume au dispărut fără urmă în anii 1944-1945, când Ecaterina se afla în refugiu la Roman. Dispărute sunt şi cele două inventare ale Bibliotecii pe care Ecaterina Sturza Cantacuzino le-a predat Episcopiei de Roman, de unde au fost ulterior luate de către Securitate. Se ştie cu certitudine că sute şi sute de cărţi au fost distruse cu barbarie, fiind arse în sobe pentru încălzire de către soldaţii ruşi care au locuit temporar în Castel, ori folosite ca ambalaje în prăvăliile din Târgu Frumos.</p>
                <p>În perioada celui de-al doilea război mondial Castelul a fost abandonat, deoarece frontul era foarte aproape de el. În această situaţie Castelul a fost devastat, fiind furate, pe lângă cărţile şi colecţiile menţionate, toată mobila şi bunurile de valoare. Faptul că biserica este înfrumuseţată acum cu preţioasele obiecte liturgice şi cărţi de cult dăruite de sturzeşti se datorează în exclusivitate Ecaterinei, care la plecarea în refugiu a luat cu ea cele mai valoroase obiecte, pe care le-a donat apoi Mănăstirii înfiinţate. Credinţa acestei femei extraordinare a împiedicat, astfel, profanarea bisericii în care strămoşii ei au înălţat pioase rugi către Dumnezeu, şi în care, de acum înainte, Domnul va fi preaslăvit de monahiile şi surorile Mănăstirii!</p>
                
