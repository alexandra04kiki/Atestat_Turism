                <p>Biblioteca Miclăuşeană s-a constituit în timp, prin achiziţii făcute de fiecare castelan, cei mai pasionaţi bibliofili fiind logofătul Dimitrie Sturza (1756-1846), fiii săi Costache Sturza-Şcheianu (cca. 1790-1860) şi Alexandru (Alecu) Sturza-Miclăuşanu (1803-1848, al cărui mormânt se află în biserică), precum şi fiul celui din urmă, Dimitrie A. Sturza (1833-1914, mare numismat şi istoric, secretar şi preşedinte al Academiei Române. Este fratele lui George, cel care a construit actualul castel). Această remarcabilă bibliotecă particulară era alcătuită din aproximativ 60.000 de volume, printre care incunabule şi multe ediţii princeps sau foarte rare. Dintre cărţile româneşti existau mai toate ediţiile princeps ale secolelor al XVIII-lea şi al XIX-lea, cópii ale aproape tuturor cronicilor româneşti, precum şi tipărituri ori manuscrise religioase, în limbile slavonă şi română, din secolele XVI-XIX. În limbi străine se găseau numeroase ediţii princeps din literatura europeană, scrieri filosofice (mai ales din antichitate şi evul mediu), lucrări istorice sau cu conţinut moral şi religios, precum şi colecţii de reviste.</p>
                <p>Pentru a sublinia inestimabila valoare a acestei Biblioteci şi drama suferită de cultura românească prin pierderea ei, menţionăm că trei dintre cele cinci prime traduceri în româneşte cunoscute (de la sfârşitul secolului al XV-lea şi din secolul al XVI-lea) provin de la Miclăuşeni sau Şcheia (altă moşie a sturzeştilor, a cărei Bibliotecă a fost donată Academiei Române în 1884 de către Dimitrie C. Sturza-Şcheianu). Acestea sunt: Psaltirea Şcheiană (cca. 1500-1559), Psaltirea Voroneţeană (cca. 1500-1559) şi Codex Sturdzanus (1580-1619).</p>
