                <p>Actualul castel, monument istoric de artă gotică, a fost ridicat între anii 1880 – 1904 de către George A. Sturza şi soţia sa Maria (născută Ghica) pe locul unui vechi conac din 1755 (din care au mai păstrat câteva odăiţe şi inscripţia de atestare). Majoritatea decoraţiilor exterioare ale castelului reiau detalii din stema sturzeştilor, la care se adaugă emblema castelanului George A. Sturza, inspirată de icoana Sfântului Gheorghe (aceeaşi emblemă apare şi pe placa de marmură a mormântului castelanului, aflat în stânga bisericii). În interior, plafoanele şi pereţii au fost pictaţi în tempera, cu motive geometrice şi florale, sub conducerea arhitectului ieşean Reinecke. O mare parte din mobilă, uşi şi ferestre era sculptată în lemn de trandafir. Pentru scara centrală a fost adusă marmură din Dalmaţia, iar la sobe s-a folosit teracotă sculptată şi pictată.Castelul deţinea o vastă bibliotecă şi câteva colecţii: 1. colecţia de arme şi costume medievale complete, pentru cal şi călăreţ, din secolul al XVI-lea (dispărută); 2. colecţia de bijuterii (dispărută); 3. Museum Familiarum, care conţinea piese rare arheologice, numismatice şi epigrafice (după 1944, au mai rămas doar câteva pietre tombale, inscripţii şi o stemă domnească) şi 4. o colecţie de busturi în marmură de Carrara şi de tablouri vechi în ulei, reprezentând strămoşi ai familiei Sturza (dispărută după 1945).</p>
